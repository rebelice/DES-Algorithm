const int P[32] = {15, 6, 19, 20, 28, 11, 27, 16, 0, 14, 22, 25, 4, 17, 30, 9, 1, 7, 23, 13, 31, 26, 2, 8, 18, 12, 29, 5, 21, 10, 3, 24};

static int C[17][28], D[17][28], L[17][32], R[17][32], T[17][48];

void leftShift(int* A, int t, int* B)
{
	int i;
	for (i = 0; i < 28; i++)
		B[i] = A[(i+t) % 28];
}

void makeT(int r)
{
	int i;
	int tmp[56];
	for (i = 0; i < 28; i++)
		tmp[i] = C[r][i];
	for (i = 0; i < 28; i++)
		tmp[i+28] = D[r][i];
	for (i = 0; i < 48; i++)
		T[r][i] = tmp[PC[i]]; 
}

void keyPermutation(int* K)
{
	int i;
	for (i = 0; i < 28; i++)
		C[0][i] = K[IPC[i]];
	for (i = 0; i < 28; i++)
		D[0][i] = K[IPC[i + 28]];
	
	for (i = 0; i < TR; i++)
	{
		leftShift(C[i], LS[i], C[i+1]);
		leftShift(D[i], LS[i], D[i+1]);
		makeT(i+1);
	}
}

void initPermutation(int* M)
{
	int i;
	for (i = 0; i < 32; i++)
		L[0][i] = M[IP[i]];
	for (i = 0; i < 32; i++)
		R[0][i] = M[IP[i + 32]];
}

void makeL(int r)
{
	int i;
	for (i = 0; i < 32; i++)
		L[r][i] = R[r-1][i];
}

void f(int* A, int* B, int* C)
{
	int i, j, x, y;
	int tmp[48], tmp2[32];
	for (i = 0; i < 48; i++)
		tmp[i] = A[E[i]] ^ B[i];
	
	for (i = 0; i < 8; i++)
	{
		x = (tmp[i*6] << 1) + tmp[i*6+5];
		y = 0;
		for (j = 1; j < 5; j++)
			y = (y << 1) + tmp[i*6+j];
		for (j = 0; j < 4; j++)
			tmp2[i*4+j] = (S[i][x][y] >> (3-j)) & 1;
	}

	for (i = 0; i < 32; i++)
		C[i] = tmp2[P[i]];
}

void makeR(int r)
{
	int i;
	int tmp[32];
	f(R[r-1], T[r], tmp);
	for (i = 0; i < 32; i++)
		R[r][i] = L[r-1][i] ^ tmp[i];
}

void inv_makeR(int r)
{
	int i;
	int tmp[32];
	f(R[r-1], T[TR-r+1], tmp);
	for (i = 0; i < 32; i++)
		R[r][i] = L[r-1][i] ^ tmp[i];
}

void inv_desRound(int round)
{
	makeL(round+1);
	inv_makeR(round+1);
}

void desRound(int round)
{

	makeL(round+1);
	makeR(round+1);
}

void last_inv_IP(int* A)
{
	int tmp[64], i;
	for (i = 0; i < 32; i++)
		tmp[i] = R[TR][i];
	for (i = 0; i < 32; i++)
		tmp[i+32] = L[TR][i];
	for (i = 0; i < 64; i++)
		A[i] = tmp[inv_IP[i]];
}

void desEncrypt(int* M, int* K, int* C)
{
	int i;
	keyPermutation(K);
	initPermutation(M);
	
	for (i = 0; i < TR; i++)
		desRound(i);

	last_inv_IP(C);
}

void desDecrypt(int* C, int* K, int* M)
{
	int i;
	keyPermutation(K);
	initPermutation(C);

	for (i = 0; i < TR; i++)
		inv_desRound(i);

	last_inv_IP(M);
}
